package eu.play_project.play_eventadapter;

public class NoRdfEventException extends Exception {

	public NoRdfEventException() {
		super();
	}

	public NoRdfEventException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoRdfEventException(Throwable cause) {
		super(cause);
	}

	public NoRdfEventException(String message) {
		super(message);
	}

	private static final long serialVersionUID = 96080518658232287L;

}
